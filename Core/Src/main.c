void pwm_config(uint8_t channel, uint32_t freq,uint16_t duty, uint8_t state)
{
	TIM_OC_InitTypeDef sConfigOC = {0};

	uint8_t psc = (SYS_FREQ/freq)>>16;

	htim1.Instance = TIM1;
	htim1.Init.Prescaler = psc;
	htim1.Init.CounterMode = TIM_COUNTERMODE_UP;
	htim1.Init.Period = SYS_FREQ/((htim1.Init.Prescaler+1)*freq);
	htim1.Init.ClockDivision = TIM_CLOCKDIVISION_DIV1;
	htim1.Init.RepetitionCounter = 0;
	htim1.Init.AutoReloadPreload = TIM_AUTORELOAD_PRELOAD_ENABLE;
	htim1.State=state;
	HAL_TIM_PWM_Init(&htim1);

	sConfigOC.OCMode = TIM_OCMODE_PWM1;
	sConfigOC.Pulse = htim1.Init.Period*duty/10000;
	sConfigOC.OCPolarity = TIM_OCPOLARITY_HIGH;
	sConfigOC.OCNPolarity = TIM_OCNPOLARITY_HIGH;
	sConfigOC.OCFastMode = TIM_OCFAST_DISABLE;
	sConfigOC.OCIdleState = TIM_OCIDLESTATE_RESET;
	sConfigOC.OCNIdleState = TIM_OCNIDLESTATE_RESET;

	uint32_t ch=(channel==0)?0:(channel<<2);
	HAL_TIM_PWM_ConfigChannel(&htim1, &sConfigOC, ch);
	if(state == ENABLE)
	{
		HAL_TIM_PWM_Start(&htim1,ch);
		HAL_TIMEx_PWMN_Start(&htim1,ch);
	}
	else
	{
		HAL_TIM_PWM_Stop(&htim1,ch);
		HAL_TIMEx_PWMN_Stop(&htim1,ch);
	}
}

